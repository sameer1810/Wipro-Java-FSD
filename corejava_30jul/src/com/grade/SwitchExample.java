package com.grade;


public class SwitchExample {

	public static void main(String[] args) {
		
		int age = 20;
		
		switch(age) {
		case 10 :
			System.out.println("Age is : 10");
			break;
		case 20 :
			System.out.println("Age is : 20");
			break;
		case 30 :
			System.out.println("Age is : 30");
			break;
			
		default:
			System.out.println("Age is not 10 or 20 or 30");
			
		}

	}

}
